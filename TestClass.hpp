namespace Math
{
    template <class T>
    T Add(T first, T second)
    {
        return first + second;
    }

    template <class T>
    T Subtract(T first, T second)
    {
        return first - second;
    }

    template <class T>
    T Multiply(T first, T second)
    {
        return first * second;
    }

    template <class T>
    T Divide(T first, T second)
    {
        return first / second;
    }
}